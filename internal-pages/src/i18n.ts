type I18nKey = keyof typeof en;
type Locale = "en" | "zh";
export type LocalePreference = Locale | "default";

const dictionaries: Record<Locale, Record<I18nKey, string>> = {
  en,
  zh,
};

export const locale: Locale = resolveLocale();

export function t(key: I18nKey, values: Record<string, string | number> = {}): string {
  const template = dictionaries[locale][key] || en[key] || key;
  return template.replace(/\{(\w+)}/g, (match, name) => {
    const value = values[name];
    return value === undefined ? match : String(value);
  });
}

export function matchesI18nText(key: I18nKey, query: string): boolean {
  const needle = query.trim().toLowerCase();
  if (!needle) return true;
  return t(key).toLowerCase().includes(needle);
}

export function getLocalePreference(): LocalePreference {
  try {
    const value = window.localStorage.getItem("hyper.locale");
    return value === "zh" || value === "en" ? value : "default";
  } catch {
    return "default";
  }
}

export function setLocalePreference(value: LocalePreference): void {
  try {
    if (value === "default") {
      window.localStorage.removeItem("hyper.locale");
    } else {
      window.localStorage.setItem("hyper.locale", value);
    }
  } catch {
    // Ignore storage failures; the UI will keep using the current locale.
  }
}

function resolveLocale(): Locale {
  const storedLocale = getLocalePreference();
  if (storedLocale === "zh" || storedLocale === "en") return storedLocale;
  const explicitLocale = readHashParam("locale");
  const candidate = explicitLocale || navigator.language || navigator.languages?.[0] || "";
  return candidate.toLowerCase().startsWith("zh") ? "zh" : "en";
}
